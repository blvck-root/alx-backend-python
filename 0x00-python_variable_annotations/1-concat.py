#!/usr/bin/env python3
"""Type-annotated concat function."""


def concat(str1: str, str2: str) -> str:
    """
    Type-annotated concat function.

    Args:
        str1 (str): The first string.
        str2 (str): The second string.

    Returns:
        str: The concatenation of str1 and str1.
    """
    return str1 + str2
